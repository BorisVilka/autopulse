package ru.autopulse05.android.feature.laximo.data.remote.dto

import ru.autopulse05.android.feature.laximo.domain.model.LaximoVehicle


data class LaximoVehicleDto(
  val vehicleId: String,
  val name: String,
  val brand: String,
  val ssd: String,
  val catalog: String?,
  val model: String?,
  val grade: String?,
  val transmission: String?,
  val doors: Int?,
  val creationRegion: String?,
  val destinationRegion: String?,
  val date: String?,
  val manufactured: Int?,
  val frameColor: String?,
  val trimColor: String?,
  val dateFrom: String?,
  val dateTo: String?,
  val frame: String?,
  val frames: String?,
  val frameFrom: String?,
  val frameTo: String?,
  val engine: String?,
  val engine1: String?,
  val engine2: String?,
  val engineNo: String?,
  val options: String?,
  val modelYearFrom: String?,
  val modelYearTo: String?,
  val modification: String?,
  val description: String?
)

fun LaximoVehicleDto.toLaximoVehicle() = LaximoVehicle(
  id = vehicleId,
  name = name,
  brand = brand,
  ssd = ssd,
  catalog = catalog,
  model = model,
  grade = grade,
  transmission = transmission,
  doors = doors,
  creationRegion = creationRegion,
  destinationRegion = destinationRegion,
  date = date,
  manufactured = manufactured,
  frameColor = frameColor,
  trimColor = trimColor,
  dateFrom = dateFrom,
  dateTo = dateTo,
  frame = frame,
  frames = frames,
  frameFrom = frameFrom,
  frameTo = frameTo,
  engine = engine,
  engine1 = engine1,
  engine2 = engine2,
  engineNo = engineNo,
  options = options,
  modelYearFrom = modelYearFrom,
  modelYearTo = modelYearTo,
  modification = modification,
  description = description
)
